 private void listartipo_unidad(){
        ResultSet rs = null;
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        cbotipo.setModel(modelo);
        try {
            rs=obj.listartipo();
            while(rs.next()){
                modelo.addElement(rs.getString("nombretipo"));
           }
        } catch (Exception e) {
        }
    }
    
    private void listarmarca(){
        ResultSet rs = null;
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        cbomarca.setModel(modelo);
        try {
            rs=obj.listarmarca();
            while(rs.next()){
                modelo.addElement(rs.getString("nombre"));
           }
        } catch (Exception e) {
        }
    }    
    
